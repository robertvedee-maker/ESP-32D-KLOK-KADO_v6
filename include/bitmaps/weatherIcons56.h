inline const unsigned char* getIcon56(int id, bool isDay) {
    // --- 800: Helder ---
    if (id == 800) return isDay ? icon_56_800D : icon_56_800N;

    // --- 801-802: Licht bewolkt ---
    if (id == 801 || id == 802) return isDay ? icon_56_801D : icon_56_801N;

    // --- 803-804: Zwaar bewolkt ---
    if (id == 803 || id == 804) return icon_56_801;

    // --- 200-232: Onweer ---
    if (id >= 200 && id <= 232) return isDay ? icon_56_200D : icon_56_200N;

    // --- 300-321 & 500-531: Regen Groep ---
    if ((id >= 300 && id <= 321) || (id >= 500 && id <= 531)) {
        if (id == 300 || id == 310 || id == 500) return isDay ? icon_56_501D : icon_56_501N; // Licht
        if (id == 301 || id == 311 || id == 501) return isDay ? icon_56_502D : icon_56_502N; // Matig
        if (id == 302 || id == 502) return isDay ? icon_56_503D : icon_56_503N; // Intens
        if (id == 312 || id == 503 || 504) return isDay ? icon_56_504D : icon_56_504N; // Zwaar
        if (id == 511) return isDay ? icon_56_511D : icon_56_511N; // IJzel
        if (id == 313 || id == 314 || id == 321 || id == 521 || id == 522) return isDay ? icon_56_521D : icon_56_521N; // Douche
        return isDay ? icon_56_502D : icon_56_502N; // Buien
    }

    // --- 600-622: Sneeuw ---
    if (id >= 600 && id <= 622) {
        if (id == 600 || id == 601) return isDay ? icon_56_600D : icon_56_600N;
        return icon_56_600;
    }

    // --- 701-781: Mist / Atmosfeer ---
    if (id >= 701 && id <= 781) {
        if (id >= 701 && id <= 731) return isDay ? icon_56_701D : icon_56_701N;
        if (id >= 741 && id <= 771) return isDay ? icon_56_741D : icon_56_741N;
        if (id == 781) return icon_56_781; // Tornado
        return icon_56_741;
    }

    // --- FAILSAFE ---
    return icon_56_801;
}
